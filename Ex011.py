Altura=float(input('A altura da parede?'))
Largura=float(input('A largura da parede?'))
Área=Largura*Altura
print('Sua parede possui uma dimensão de {:.2f}x{:.2f}, resultando em {:.2f}m^2.'.format(Altura,Largura,Área))
Tinta= Área/2
print('Você utilizando uma área de {:.2f}m^2, você gasta {:.2f} litros de tinta.'.format(Área,Tinta))







